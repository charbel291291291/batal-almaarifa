const catalogs: Record<Locale, Record<Key, string>> = { ar, en, fr };

export function translate(locale: Locale, key: Key, params: Params = {}): string {
  return catalogs[locale][key].replace(/\{(\w+)\}/g, (_, name: string) => String(params[name] ?? `{${name}}`));
}

export function categoryLabel(category: CategoryId, locale: Locale): string {
  return translate(locale, `category_${category}` as Key);
}

export function difficultyLabel(difficulty: Difficulty, locale: Locale): string {
  return translate(locale, `difficulty_${difficulty}` as Key);
}

export function roundLabel(round: RoundId, locale: Locale): string {
  return translate(locale, `round_${round}` as Key);
}

